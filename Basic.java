public class Basic {
	public static void main(String[] args) {
		System.out.println(42);
		System.out.println(42 + 83);
		System.out.println(125 - 42);
		System.out.println(125 * 42);
		System.out.println(125 / 10);
		System.out.println(125 % 10); 
		System.out.println(2 + 3 * 4);
		System.out.println(2 * 3 + 4);
		System.out.println((2 + 3) * 4);
		System.out.println(2 + (3 * 4));
		System.out.println(7 >= 7);
	}
}